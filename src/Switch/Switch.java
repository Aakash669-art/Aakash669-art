package Switch;

public class Switch {
    public static void main(String[] args) {

        char code ='C';
        switch (code) {
            default:
                System.out.print("Hello");
                break;
            case 'A':
            System.out.print("65");
            break;
            case 'B':
            System.out.print("66");
            break;
        }
    }
}
